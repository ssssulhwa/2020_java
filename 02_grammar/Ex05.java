class Ex05 {
	public static void main(String[] args) {
		// 실수: 소숫점 있음
		// flaoat < double
		// 실수의 기본 double

		// float: 숫자 뒤 반드시 F,f 붙이기 (생략하면 오류)
		// 24가 정수니까 float는 실수, 그러므로 저장 가능
		float su1 = 24;
		System.out.println(su1);

		// 31.4에 f,F를 붙여아 오류 안남
		float su2 = 31.4f;
		System.out.println(su2);

		// char c1 = 97 ;
		char c1 = 97;
		System.out.println(c1);

		float su3 = c1;
		System.out.println(su3);

		// double은 byte, short, char, int, long, float 모두 받아서 저장 가능
		double su4 = 3.14;
		System.out.println(su4);

		double su5 = su3;
		System.out.println(su5);

		double su6 = c1;
		System.out.println(su6);

	}
}
